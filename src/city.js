class City {

    init(divinity, commerce, troop){
        this.divinity = divinity;
        this.commerce = commerce;
        this.troop = troop;
    }

    get divinity() {
        return this.divinity;
    }

    get commerce() {
        return this.commerce;
    }

    get troop() {
        return this.troop;
    }

}